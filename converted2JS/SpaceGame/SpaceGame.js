  *whenGreenFlagClicked() {
    this.goto(0, 0);
    this.costume = "spaceship";
    this.size = 100;
    this.visible = true;
    this.createClone();
    this.visible = false;
    this.vars.numStars = 1000;
    this.vars.worldSize = 1000;
    this.vars.distanceToViewingPlane = 100;
    yield* this.initialiseStars();
    yield* this.initialiseLasers();
    while (true) {
      this.clearPen();
      this.vars.speed = this.mouse.y / 6;
      this.vars.speedOfRoll = this.mouse.x / 40;
      if (this.mouse.down) {
        if (this.vars.firePressed == "false") {
          yield* this.fireLasers();
          this.vars.firePressed = "true";
        }
      } else {
        this.vars.firePressed = "false";
      }
      yield* this.drawStars();
      yield* this.drawLasers();
      yield* this.updateStars();
      yield* this.updateLasers();
      yield;
    }
  }

  *drawStars() {
    this.penColor = Color.rgb(255, 255, 255);
    this.penSize = 2;
    this.vars.index = 1;
    this.size = 1000;
    for (let i = 0; i < this.vars.numStars; i++) {
      this.vars.worldX = this.vars.starX[this.vars.index - 1];
      this.vars.worldY = this.vars.starY[this.vars.index - 1];
      this.vars.worldZ = this.vars.starZ[this.vars.index - 1];
      this.penColor.v = 100 * (1 - this.vars.worldZ / this.vars.worldSize);
      this.warp(this.perspectiveTransformXYZ)(
        this.vars.worldX,
        this.vars.worldY,
        this.vars.worldZ
      );
      this.goto(this.vars.screenX, this.vars.screenY);
      this.penDown = true;
      this.goto(
        this.vars.previousScreenX[this.vars.index - 1],
        this.vars.previousScreenY[this.vars.index - 1]
      );
      this.penDown = false;
      this.vars.previousScreenX.splice(
        this.vars.index - 1,
        1,
        this.vars.screenX
      );
      this.vars.previousScreenY.splice(
        this.vars.index - 1,
        1,
        this.vars.screenY
      );
      this.vars.index += 1;
    }
  }

  *updateStars() {
    this.vars.index = 1;
    for (let i = 0; i < this.vars.numStars; i++) {
      this.vars.worldZ = this.vars.starZ[this.vars.index - 1] - this.vars.speed;
      this.vars.starZ.splice(this.vars.index - 1, 1, this.vars.worldZ);
      if (this.vars.worldZ < 1) {
        this.warp(this.placeNewStarWithIndexAtDepth)(
          this.vars.index,
          this.vars.worldSize
        );
      } else {
        if (this.vars.worldZ > this.vars.worldSize) {
          this.warp(this.placeNewStarWithIndexAtDepth)(this.vars.index, 1);
        } else {
          this.warp(this.zAxisRotationStarAtIndexByAngle)(
            this.vars.index,
            this.vars.speedOfRoll
          );
        }
      }
      this.vars.index += 1;
    }
  }

  *placeNewStarWithIndexAtDepth(index2, z) {
    this.vars.worldX = this.random(
      0 - this.vars.worldSize,
      this.vars.worldSize
    );
    this.vars.worldY = this.random(
      0 - this.vars.worldSize,
      this.vars.worldSize
    );
    this.vars.worldZ = z;
    this.vars.starX.splice(index2 - 1, 1, this.vars.worldX);
    this.vars.starY.splice(index2 - 1, 1, this.vars.worldY);
    this.vars.starZ.splice(index2 - 1, 1, this.vars.worldZ);
    this.warp(this.perspectiveTransformXYZ)(
      this.vars.worldX,
      this.vars.worldY,
      this.vars.worldZ
    );
    this.vars.previousScreenX.splice(index2 - 1, 1, this.vars.screenX);
    this.vars.previousScreenY.splice(index2 - 1, 1, this.vars.screenY);
  }

  *perspectiveTransformXYZ(x, y, z2) {
    this.vars.screenX = (this.vars.distanceToViewingPlane / z2) * x;
    this.vars.screenY = (this.vars.distanceToViewingPlane / z2) * y;
  }

  *drawLasers() {
    this.penColor = Color.rgb(255, 0, 0);
    this.vars.index = 1;
    for (let i = 0; i < this.vars.laserX.length; i++) {
      this.vars.worldX = this.vars.laserX[this.vars.index - 1];
      this.vars.worldY = this.vars.laserY[this.vars.index - 1];
      this.vars.worldZ = this.vars.laserZ[this.vars.index - 1];
      this.warp(this.perspectiveTransformXYZ)(0, 20, this.vars.worldZ);
      this.penSize = this.vars.screenY;
      this.penColor.v = 100 * (1 - this.vars.worldZ / this.vars.worldSize);
      this.warp(this.perspectiveTransformXYZ)(
        this.vars.worldX,
        this.vars.worldY,
        this.vars.worldZ
      );
      this.goto(this.vars.screenX, this.vars.screenY);
      this.penDown = true;
      this.penDown = false;
      this.vars.index += 1;
    }
  }

  *initialiseLasers() {
    this.vars.laserX = [];
    this.vars.laserY = [];
    this.vars.laserZ = [];
    this.vars.firePressed = "true";
  }

  *updateLasers() {
    this.vars.index = this.vars.laserX.length;
    for (let i = 0; i < this.vars.laserX.length; i++) {
      this.vars.worldZ = this.vars.laserZ[this.vars.index - 1] + 40;
      this.vars.laserZ.splice(this.vars.index - 1, 1, this.vars.worldZ);
      if (this.vars.worldZ > this.vars.worldSize) {
        this.vars.laserX.splice(this.vars.index - 1, 1);
        this.vars.laserY.splice(this.vars.index - 1, 1);
        this.vars.laserZ.splice(this.vars.index - 1, 1);
      } else {
        this.warp(this.zAxisRotationLaserAtIndexByAngle)(
          this.vars.index,
          this.vars.speedOfRoll
        );
      }
      this.vars.index += -1;
    }
  }

  *fireLasers() {
    yield* this.startSound("laser");
    this.vars.worldX = -122;
    this.vars.worldY = -82;
    this.vars.worldZ = this.vars.distanceToViewingPlane;
    this.vars.laserX.push(this.vars.worldX);
    this.vars.laserY.push(this.vars.worldY);
    this.vars.laserZ.push(this.vars.worldZ);
    this.vars.worldX = 122;
    this.vars.laserX.push(this.vars.worldX);
    this.vars.laserY.push(this.vars.worldY);
    this.vars.laserZ.push(this.vars.worldZ);
  }

  *whenGreenFlagClicked2() {
    while (true) {
      yield* this.playSoundUntilDone("star wars");
      yield;
    }
  }

  *zAxisRotationStarAtIndexByAngle(index3, angle) {
    this.vars.worldX = this.vars.starX[this.vars.index - 1];
    this.vars.worldY = this.vars.starY[this.vars.index - 1];
    this.vars.starX.splice(
      this.vars.index - 1,
      1,
      this.vars.worldX * Math.cos(this.degToRad(angle)) -
        this.vars.worldY * Math.sin(this.degToRad(angle))
    );
    this.vars.starY.splice(
      this.vars.index - 1,
      1,
      this.vars.worldX * Math.sin(this.degToRad(angle)) +
        this.vars.worldY * Math.cos(this.degToRad(angle))
    );
  }

  *zAxisRotationLaserAtIndexByAngle(index4, angle2) {
    this.vars.worldX = this.vars.laserX[this.vars.index - 1];
    this.vars.worldY = this.vars.laserY[this.vars.index - 1];
    this.vars.laserX.splice(
      this.vars.index - 1,
      1,
      this.vars.worldX * Math.cos(this.degToRad(angle2)) -
        this.vars.worldY * Math.sin(this.degToRad(angle2))
    );
    this.vars.laserY.splice(
      this.vars.index - 1,
      1,
      this.vars.worldX * Math.sin(this.degToRad(angle2)) +
        this.vars.worldY * Math.cos(this.degToRad(angle2))
    );
  }
